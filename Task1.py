"""_1) Когда используется критерий Стьюдента, а когда Z –критерий?
"""

"""Критерий Стьюдента обычно используется, когда мы работаем с малыми выборками (обычно менее 30 наблюдений) и 
не знаем параметры генеральной совокупности, такие как стандартное отклонение."""

"""Z-критерий (Z-тест) используется, когда мы имеем большие выборки (обычно более 30 наблюдений) и 
знаем параметры генеральной совокупности, такие как стандартное отклонение."""